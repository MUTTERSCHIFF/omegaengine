using System;
using TerrainSample.World.Positionables;

namespace TerrainSample.World.Pathfinding
{
    /// <summary>
    /// Controls how an <see cref="Entity{TCoordinates}"/> will move along a path generated by pathfinding.
    /// </summary>
    /// <typeparam name="TCoordinates">Coordinate data type (2D, 3D, ...)</typeparam>
    /// <seealso cref="Entity{TCoordinates}.PathControl"/>
    public abstract class PathControl<TCoordinates> : ICloneable
        where TCoordinates : struct
    {
        /// <inheritdoc/>
        public override string ToString()
        {
            return GetType().Name;
        }

        #region Clone
        /// <summary>
        /// Creates a shallow copy of this <see cref="PathControl{TCoordinates}"/>
        /// </summary>
        /// <returns>The cloned <see cref="PathControl{TCoordinates}"/>.</returns>
        public PathControl<TCoordinates> Clone()
        {
            // Perform initial shallow copy
            return (PathControl<TCoordinates>)MemberwiseClone();
        }

        object ICloneable.Clone()
        {
            return Clone();
        }
        #endregion
    }
}
